namespace TrailCommon
{
    /// <summary>
    ///     Amount of food people in party eat each day can change.
    /// </summary>
    public enum RationLevel
    {
        /// <summary>
        ///     Meals are large and generous.
        /// </summary>
        Filling,

        /// <summary>
        ///     Meals are small, but adequate.
        /// </summary>
        Meager,

        /// <summary>
        ///     Meals are very small; everyone stays hungry.
        /// </summary>
        BareBones
    }
}